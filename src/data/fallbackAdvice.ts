export function getFallbackAdvice(season: Season, question: string): string {
  const variants = FALLBACK_BY_SEASON[season] ?? FALLBACK_BY_SEASON.лето;
  // Берем вариант, чей текст лучше соответствует вопросу
  const lowerQ = question.toLowerCase();
  let bestIdx = 0;
  let bestScore = -1;
  variants.forEach((v, i) => {
    const score = countMatches(v.toLowerCase(), lowerQ);
    if (score > bestScore) {
      bestScore = score;
      bestIdx = i;
    }
  });
  const answer = variants[bestIdx] ?? variants[0]!;
  return answer + '\n\n_Это совет из локальной базы. Чтобы получить персональный ответ от ИИ, пополните баланс на https://platform.deepseek.com/._';
}

function countMatches(text: string, query: string): number {
  const words = query.split(/\s+/).filter((w) => w.length > 3);
  if (words.length === 0) return 0;
  let count = 0;
  for (const w of words) {
    if (text.includes(w)) count++;
  }
  return count;
}
