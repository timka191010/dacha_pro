export const getProductById = (id: string): Product | undefined =>
  products.find((p) => p.id === id);

export const getProductsByIds = (ids: string[]): Product[] =>
  ids.map(getProductById).filter((p): p is Product => Boolean(p));

export const getProductsForMonth = (month: number): Product[] =>
  products.filter((p) => p.seasons?.some((s) => seasonMatchesMonth(s, month)));

function seasonMatchesMonth(season: string, month: number): boolean {
  if (season === 'весна') return month >= 3 && month <= 5;
  if (season === 'лето') return month >= 6 && month <= 8;
  if (season === 'осень') return month >= 9 && month <= 11;
  if (season === 'зима') return month === 12 || month <= 2;
  return false;
}
